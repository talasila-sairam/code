$(document).ready(function() {
	$("#userAssignment").on('click', function(e){
		e.preventDefault();
		$("#submenuPagesDisplay").removeAttr("src");
		$("#submenuPagesDisplay").attr("src","./UserAssignmentReports.jsp");
	});
	$("#errorlist").on('click', function(e){
		e.preventDefault();
		$("#submenuPagesDisplay").removeAttr("src");
		$("#submenuPagesDisplay").attr("src","./UserReport.jsp");
	})
});